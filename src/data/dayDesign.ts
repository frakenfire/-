// 톤: 토스 라이팅 원칙 — 해요체, 공감 먼저, 긍정형, 잡초 없이, 어미 다양화.

export type MoodGroup = 'up' | 'flat' | 'down';

// 기분 5종을 상태 3군으로 모은다 (/goal: 좋은 사람 vs 심신이 안 좋은 사람).
export function moodGroup(m: Mood): MoodGroup {
  if (m === 'good') return 'up';
  if (m === 'soso') return 'flat';
  return 'down'; // tired · anxious · lonely
}

